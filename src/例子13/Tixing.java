package 例子13;

public class Tixing {
    double height,bottom,length,area;
    void setHeight(double a,double b,double c){
        height=a;
        bottom=b;
        length=c;
    }
    double getarea(){
        height=height;
        bottom=bottom;
        length=length;
        area=(length+bottom)*height/2;
        return area;

    }
}
